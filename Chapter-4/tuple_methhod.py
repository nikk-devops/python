a = (1, 7, 2)
a.count (1) #a count (1) will return number of times 1 occurs in a.
a.index (1) #will return the index of first occurrence of 1 in a.

print(a.count(1))  # Output: 1
print(a.index(1))  # Output: 0
# Note: Tuples do not have methods like sort(), reverse(), append(), insert(), pop(), or remove() because they are immutable.
