a =  ("024356789")
a[1:6:4] # Slicing the string from index 1 to 6 with a step of 4
print(a[1:6:4])
print(a[::-1])  # Reversing the string using slicing
print(a[::2])   # Slicing the string to get every second character
print(a[-3:-1])  # Slicing the string using negative indices
print(a[3:])    # Slicing the string from index 3 to the end
