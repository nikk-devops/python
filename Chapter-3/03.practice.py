name = (" Hridyansh is a  good  boy and  I  love him  ")

print(name.count("  ")) # Counting double spaces in the string
print(name.replace("  ", " ")) # Replacing double spaces with single space
print(name.find("  ")) # Finding the index of first occurrence of double spaces
print(name.index("  ")) # Finding the index of first occurrence of double spaces using index method

