name = "Hridyansh"

print(len(name))  # Output: 9
print(name.lower())  # Output: hridyansh
print(name.upper())  # Output: HRIDYANSH
print(name.replace("Hridyansh", "CodeWithHarry"))  # Output: CodeWithHarry
print(name.count("h"))  # Output: 1
print(name.index("y"))  # Output: 4
print(name.capitalize())  # Output: Hridyansh
print(name.title())  # Output: Hridyansh        
print(name.startswith("H"))  # Output: True
print(name.endswith("h"))  # Output: True
print(name.split("y"))  # Output: ['Hrid', 'ansh']
print(name.find("d"))  # Output: 5




